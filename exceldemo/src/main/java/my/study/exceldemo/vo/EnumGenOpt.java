package my.study.exceldemo.vo;


public  class EnumGenOpt{
	private String pkgName;
	private String className;
	private int keyIdx;
	private int valIdx;
	private int enumItemNameIdx;
	
	public String getPkgName() {
		return pkgName;
	}
	public void setPkgName(String pkgName) {
		this.pkgName = pkgName;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public int getKeyIdx() {
		return keyIdx;
	}
	public void setKeyIdx(int keyIdx) {
		this.keyIdx = keyIdx;
	}
	public int getValIdx() {
		return valIdx;
	}
	public void setValIdx(int valIdx) {
		this.valIdx = valIdx;
	}
	public int getEnumItemNameIdx() {
		return enumItemNameIdx;
	}
	public void setEnumItemNameIdx(int enumItemNameIdx) {
		this.enumItemNameIdx = enumItemNameIdx;
	}
		
}